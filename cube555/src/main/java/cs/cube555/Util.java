	static int getParity(int idx, int n) {
		int parity = 0;
		for (int i = n - 2; i >= 0; --i) {
			parity ^= idx % (n - i);
			idx /= n - i;
		}
		return parity & 1;
	}

	static int getParity(int[] arr) {
		int parity = 0;
		for (int i = 0; i < arr.length - 1; i++) {
			for (int j = i + 1; j < arr.length; j++) {
				if (arr[i] > arr[j]) {
					parity ^= 1;
				}
			}
		}
		return parity;
	}


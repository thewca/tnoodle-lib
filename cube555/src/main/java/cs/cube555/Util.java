	static int getPerm(int[] arr, int n, boolean even) {
		int idx = 0;
		long val = 0xFEDCBA9876543210L;
		for (int i = 0; i < n - 1; ++i) {
			int v = arr[i] << 2;
			idx = (n - i) * idx + (int) (val >> v & 0xf);
			val -= 0x1111111111111110L << v;
		}
		return even ? (idx >> 1) : idx;
	}


	static int[] setComb(int[] arr, int idx, int r, int n) {
		for (int i = n - 1; i >= 0; i--) {
			if (idx >= Cnk[i][r]) {
				idx -= Cnk[i][r--];
				arr[i] = 0;
			} else {
				arr[i] = -1;
			}
		}
		return arr;
	}

